#ifndef FACTORIAL_H_
#define FACTORIAL_H_

/*!
 * Calculates the factorial of a number using recursive programming.
 *
 * @param n the number
 * @return the factorial
 */
int factorialRecurs(int n);

/*!
 * Calculates the factorial of a number using dynamic programming.
 *
 * @param n the number
 * @return the factorial
 */
int factorialDinam(int n);

#endif /* FACTORIAL_H_ */
